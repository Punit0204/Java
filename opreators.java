public class opreators {
    public static void main(String[] args){
        int a = 56;
        a += 5;
        int c = a;
        System.out.println(c);
        System.out.println(45<40); // COMPARISION OPREATOR
        System.out.println(64>5 && 64<8); // AND  OPERATOR
        System.out.println(65>50 || 50>80); // OR OPREATOR
        System.out.println(5&3); // BITWISE OPREATOR
        // % CAN BE FORMED ON FLOAT AND DOUBLE;
        // EG: 4.34%1.22;
        
    }
    
}
